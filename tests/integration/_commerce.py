"""Shared helpers for vendor-execution integration tests.

Every vendor's integration test runs the **same battery of queries** against
the **same commerce data**, with DuckDB acting as the source of truth. A
mismatch between DuckDB and the target vendor means a dialect bug.

What lives here
---------------

* :data:`COMMERCE_TABLES` — parquet fixtures shipped under
  ``tests/fixtures/commerce/`` (regenerated by
  ``tests/fixtures/commerce/_export.py``).
* :func:`load_commerce_model` — loads ``examples/orionbelt_1_commerce.yaml``
  and rewrites ``database``/``schema`` on every DataObject so the compiled
  SQL targets the place where the test setup created the tables.
* :func:`open_duckdb_truth` — opens an in-memory DuckDB connection with the
  parquet fixtures attached as views in a chosen schema.
* :data:`COMMERCE_CASES` — the query battery. Each case has a stable name
  (used in test IDs) and a :class:`QueryObject`.
* :func:`fetch_rows` — execute a compiled SQL on a vendor connection and
  normalise the result to ``list[dict[str, Any]]``.
* :func:`compare_rows` — type-tolerant row diff (sort, then numeric-or-string
  compare per column).

What does **not** live here: vendor-specific connection setup, bulk-loading
parquet into the target, or schema/dataset creation. Each vendor test file
owns those pieces because they vary too much (cloud auth, testcontainers
docker, native bulk-load APIs).
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Any

import duckdb

from orionbelt.compiler.pipeline import CompilationPipeline
from orionbelt.models.query import (
    FilterOperator,
    Grouping,
    QueryFilter,
    QueryObject,
    QueryOrderBy,
    QuerySelect,
    SortDirection,
)
from orionbelt.models.semantic import SemanticModel
from orionbelt.parser.loader import TrackedLoader
from orionbelt.parser.resolver import ReferenceResolver

# ---------------------------------------------------------------------------
# Paths & table list
# ---------------------------------------------------------------------------

FIXTURES_DIR = Path(__file__).resolve().parent.parent / "fixtures" / "commerce"
COMMERCE_MODEL_YAML = Path(__file__).resolve().parents[2] / "examples" / "orionbelt_1_commerce.yaml"

# Ordered for predictable bulk-load logs. Dimension tables come first so a
# half-loaded session still resolves star-schema joins.
COMMERCE_TABLES: tuple[str, ...] = (
    # Dimensions
    "calendar",
    "countries",
    "regions",
    "clients",
    "products",
    "employees",
    "suppliers",
    "banks",
    "channels",
    "acctbal",
    # Facts
    "sales",
    "shipments",
    "purchases",
    "returns",
    "clientcomplaints",
)


def parquet_path(table: str) -> Path:
    """Absolute path to the committed parquet fixture for ``table``."""
    return FIXTURES_DIR / f"{table}.parquet"


# ---------------------------------------------------------------------------
# Model loading
# ---------------------------------------------------------------------------


def load_commerce_model(*, database: str, schema: str = "orionbelt_1") -> SemanticModel:
    """Load the commerce OBML and rewrite ``database``/``schema`` on each table.

    The model file ships without a ``database:`` field — DuckDB-style two-part
    naming is enough for the demo. Three-part dialects (BigQuery, Snowflake,
    Databricks, Dremio) need an explicit project/database, so every vendor
    test calls this helper with the value the test fixture just created the
    tables in.
    """
    loader = TrackedLoader()
    resolver = ReferenceResolver()
    raw, source_map = loader.load(COMMERCE_MODEL_YAML)
    model, result = resolver.resolve(raw, source_map)
    if not result.valid:
        raise AssertionError(
            "Commerce model failed to resolve: "
            + "; ".join(f"{e.code}: {e.message}" for e in result.errors)
        )
    for obj in model.data_objects.values():
        obj.database = database
        obj.schema_name = schema
    return model


# ---------------------------------------------------------------------------
# DuckDB truth
# ---------------------------------------------------------------------------


def open_duckdb_truth(schema: str = "orionbelt_1") -> duckdb.DuckDBPyConnection:
    """Open an in-memory DuckDB with the parquet fixtures attached as views.

    Views (not tables) keep the connection cheap to construct; reads still
    stream off the committed parquet files. The connection is the truth
    every other vendor's results are diffed against.
    """
    con = duckdb.connect(":memory:")
    con.execute(f'CREATE SCHEMA "{schema}"')
    for table in COMMERCE_TABLES:
        path = parquet_path(table)
        if not path.is_file():
            raise FileNotFoundError(
                f"Missing parquet fixture {path}. Regenerate via: "
                "uv run python tests/fixtures/commerce/_export.py"
            )
        con.execute(
            f'CREATE VIEW "{schema}"."{table}" AS SELECT * FROM read_parquet(\'{path.as_posix()}\')'
        )
    return con


