def compile_for(query: QueryObject, model: SemanticModel, dialect: str) -> str:
    """Compile ``query`` against ``model`` for ``dialect`` and return the SQL."""
    return CompilationPipeline().compile(query, model, dialect).sql


def fetch_duckdb(con: duckdb.DuckDBPyConnection, sql: str) -> list[dict[str, Any]]:
    """Execute SQL on a DuckDB connection and return rows as dicts."""
    cur = con.execute(sql)
    cols = [d[0] for d in cur.description]
    return [dict(zip(cols, row, strict=False)) for row in cur.fetchall()]


def _normalise_value(v: Any) -> Any:
    """Coerce values to comparable forms across DB drivers.

    Drivers return decimals as ``Decimal``, ``float``, ``str``, or a custom
    ``Numeric`` class depending on vendor. Dates may come as ``date``,
    ``datetime``, or ISO strings. Booleans may come as ``0``/``1`` from
    MySQL. Normalise numerics to ``float`` and everything else (including
    dates) to its ``str`` form for comparison.
    """
    if v is None:
        return None
    if isinstance(v, bool):
        return bool(v)
    if isinstance(v, int | float):
        return float(v)
    # Decimal, anything with __float__
    try:
        return float(v)  # type: ignore[arg-type]
    except (TypeError, ValueError):
        return str(v)


def _row_key(row: dict[str, Any], columns: list[str]) -> tuple:
    """Stable sort key for a result row, dialect-agnostic."""
    out = []
    for c in columns:
        v = row.get(c)
        if v is None:
            out.append((0, ""))  # nulls sort first (matches NULLS FIRST default)
        else:
            out.append((1, str(v)))
    return tuple(out)


def compare_rows(
    actual: list[dict[str, Any]],
    expected: list[dict[str, Any]],
    *,
    case: str,
    rel_tol: float = 1e-4,
    abs_tol: float = 1e-6,
) -> None:
    """Diff two result sets row by row, dialect-tolerant.

    Both sides must share column names (they do — the compiled SQL uses the
    same SELECT aliases regardless of dialect). Numeric columns are compared
    with combined relative/absolute tolerance to absorb decimal-vs-float
    rounding differences across drivers.
    """
    if len(actual) != len(expected):
        raise AssertionError(
            f"[{case}] row count mismatch: actual={len(actual)} expected={len(expected)}"
        )
    if not expected:
        return  # both empty, nothing to check
    cols = list(expected[0].keys())
    actual_sorted = sorted(actual, key=lambda r: _row_key(r, cols))
    expected_sorted = sorted(expected, key=lambda r: _row_key(r, cols))

    for i, (a_row, e_row) in enumerate(zip(actual_sorted, expected_sorted, strict=False)):
        for col in cols:
            av = _normalise_value(a_row.get(col))
            ev = _normalise_value(e_row.get(col))
            if av is None and ev is None:
                continue
            if av is None or ev is None:
                raise AssertionError(
                    f"[{case}] row {i} col {col!r}: NULL mismatch "
                    f"actual={a_row.get(col)!r} expected={e_row.get(col)!r}"
                )
            if isinstance(av, float) and isinstance(ev, float):
                diff = abs(av - ev)
                if diff > max(rel_tol * max(abs(av), abs(ev)), abs_tol):
                    raise AssertionError(
                        f"[{case}] row {i} col {col!r}: numeric mismatch "
                        f"actual={av} expected={ev} (diff={diff})"
                    )
            elif av != ev:
                raise AssertionError(
                    f"[{case}] row {i} col {col!r}: value mismatch "
                    f"actual={a_row.get(col)!r} expected={e_row.get(col)!r}"
                )


